package C.EComposition.A;

public class Etudiant {
    public String nom;
    public String prenom;
    public int no;
    public String nomDePere;
    public String nomDeMere;
    public int anneDeNaissance;


    public Etudiant(String nom, String prenom, int no, String nomDePere, String nomDeMere, int anneDeNaissance) {
        this.nom = nom;
        this.prenom = prenom;
        this.no = no;
        this.nomDePere = nomDePere;
        this.nomDeMere = nomDeMere;
        this.anneDeNaissance = anneDeNaissance;
    }





}
