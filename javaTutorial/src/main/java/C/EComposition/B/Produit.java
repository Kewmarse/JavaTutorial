package C.EComposition.B;

public class Produit {

    public String nom;
    public String category;
    public int prix;
    public String produitNo;
    public String stok;

    public Produit(String nom, String category, int prix, String produitNo, String stok) {
        this.nom = nom;
        this.category = category;
        this.prix = prix;
        this.produitNo = produitNo;
        this.stok = stok;
    }
}
